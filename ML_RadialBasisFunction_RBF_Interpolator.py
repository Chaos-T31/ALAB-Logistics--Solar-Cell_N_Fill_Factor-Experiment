from scipy.interpolate import Rbf


def RBF_Interpolator(XnY, y):                                       # Alternative Interpolation Function for Re-checking the Unvariate Spline Interpolator
    return Rbf(XnY['x'], XnY['y'])(y)

# Testing-
y = RBF_Interpolator({'x': [1, 2, 3], 'y': [3, 2, 1]}, 2.5)
print(y)



